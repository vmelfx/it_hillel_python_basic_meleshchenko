x: int = 5
y: int = 5

# Python arithmetic operators
print(x + y)  # addition
print(x - y)  # subtraction
print(x * y)  # multiplication
print(x / y)  # division
print(x % y)  # modulus
print(x ** y)  # exponentiation
print(x // y)  # floor division

# Python assignment operators
x += 3  # same as x = x + 3
print(x)
x -= 3  # same as x = x - 3
print(x)
x *= 3  # same as x = x * 3
print(x)
x /= 3  # same as x = x / 3
print(x)
